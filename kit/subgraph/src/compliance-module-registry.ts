import {
  ComplianceModuleRegistered as ComplianceModuleRegisteredEvent,
  RoleAdminChanged as RoleAdminChangedEvent,
  RoleGranted as RoleGrantedEvent,
  RoleRevoked as RoleRevokedEvent,
} from "../generated/ComplianceModuleRegistry/ComplianceModuleRegistry"
import {
  ComplianceModuleRegistered,
  RoleAdminChanged,
  RoleGranted,
  RoleRevoked,
} from "../generated/schema"

export function handleComplianceModuleRegistered(
  event: ComplianceModuleRegisteredEvent,
): void {
  let entity = new ComplianceModuleRegistered(
    event.transaction.hash.concatI32(event.logIndex.toI32()),
  )
  entity.sender = event.params.sender
  entity.name = event.params.name
  entity.typeId = event.params.typeId
  entity.moduleAddress = event.params.moduleAddress
  entity.timestamp = event.params.timestamp

  entity.blockNumber = event.block.number
  entity.blockTimestamp = event.block.timestamp
  entity.transactionHash = event.transaction.hash

  entity.save()
}

